package chapter05;

public class Printer {

	public static void println(int thing) {
		System.out.println(thing);
	}

	public static void println(double thing) {
		System.out.println(thing);
	}

	public static void println(boolean thing) {
		System.out.println(thing);
	}

	public static void println(String thing) {
		System.out.println(thing);
	}
}
